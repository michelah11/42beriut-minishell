#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	unsigned char	*arr;

	arr = (unsigned char *) s;
	while (n > 0)
	{
		arr[n - 1] = c;
		n--;
	}
	return (s);
}
